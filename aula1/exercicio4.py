# Conversor de temperatura Celsius para Fahrenheit

print("Bem vindo ao conversor de Temperatura")
print("Digite uma temperatura em Graus Celsius para converter em Fahrenheit")
celsius = float(input());
fahrehnheit = (celsius * 1.8) + 32
print("Temperatura em graus celcius: ",celsius)
print("Temperatura convertida em fahrenhieit: ",fahrehnheit)
